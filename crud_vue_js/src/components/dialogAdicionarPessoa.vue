<template>
  <q-dialog v-on="$listeners" v-bind="$attrs">
    <q-card style="width: 700px; max-width: 80vw;">
      <q-card-section>
        <div class="text-h6">Adicionar Usu치rio</div>
      </q-card-section>

      <q-card-section class="q-pt-none">
        <q-input
          outlined
          type="text"
          v-model="nomeUsuario"
          label="Nome do usu치rio"
          class="q-my-md"
        />
        <q-input
          outlined
          type="email"
          v-model="emailUsuario"
          label="E-mail do usu치rio"
          class="q-my-md"
        />
        <q-select
          outlined
          v-model="tipoUsuario"
          :options="categoriaUsuario"
          label="Tipo de usu치rio"
          class="q-my-md"
        />
      </q-card-section>

      <q-card-actions align="right">
        <q-btn flat label="Sair" color="primary" v-close-popup />
        <q-btn
          label="Criar"
          color="primary"
          v-close-popup
          @click="adicionarUsuario"
        />
      </q-card-actions>
    </q-card>
  </q-dialog>
</template>

<script>
export default {
  name: "dialogEdicaoPessoa",
  data() {
    return {
      categoriaUsuario: ["Admin", "Gerente", "Normal"],
      codigoUsuario: "",
      nomeUsuario: "",
      emailUsuario: "",
      tipoUsuario: ""
    };
  },
  methods: {
    adicionarUsuario() {
      var obj = {
        nomeUsuario: this.nomeUsuario,
        emailUsuario: this.emailUsuario,
        categoriaUsuarioInt: this.categoriaUsuarioInt
      };
      this.$emit("adicionarUsuario", obj);
    }
  },
  computed: {
    categoriaUsuarioInt() {
      var categoriaInt;
      switch (this.tipoUsuario) {
        case "Admin":
          categoriaInt = 1;
          break;
        case "Gerente":
          categoriaInt = 2;
          break;
        default:
          categoriaInt = 3;
          break;
      }
      return categoriaInt;
    }
  },
};
</script>
